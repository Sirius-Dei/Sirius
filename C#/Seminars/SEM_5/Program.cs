int Factorial(int n)
{
    //1!=1
    //0!=1
    if (n == 1) return 1;
    else return n * Factorial(n - 1);
}

int f(int n)
{
    if (n == 1 && n == 2) return 1;
    else return f(n - 1) + f(n - 2);
}
